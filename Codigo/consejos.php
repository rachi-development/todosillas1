				<p>Una buena silla de oficina ha de ser ergonómica, lo que quiere decir que se adapta completamente a tu cuerpo. Y para dar con una apta, debes seguir esta guía:- QUE SEA AJUSTABLE. Para una máxima ergonomía es necesario que todas las partes del aparato se ajusten a nuestro cuerpo. En este caso hablamos de: respaldo, asiento y altura. Normalmente las sillas regulan la altura (es importante que los pies lleguen al suelo) y permiten que los respaldos se inclinen, pero también es importante que el asiento se pueda alargar o encoger para que no roce la zona de atrás de las rodillas.- CON BRAZOS O SIN ELLOS. Algunos expertos recomiendan que no tengan reposabrazos para no entorpecer una postura en la que los brazos continúen la línea de los hombros y se mantengan pegados al cuerpo. Aun así, muchos prefieren poder apoyar los brazos en algún momento del día para descansarlos y su presencia les permite una mayor comodidad a la hora de levantarse, al mismo tiempo que ayuda al equilibrio. En el caso de que nos decantemos por una silla con reposabrazos, es recomendable que se puedan regular para adaptarse a cada persona.- CON RUEDAS. Mejor que una silla fija, para una oficina es más recomendable usar de ruedas. Esto aumenta la movilidad en la mesa y la comodidad. Así mismo, el eje de la base reparte el peso de forma diferente a una silla con patas, por lo que es importante conocer los kilos que soporta el producto que vayamos a comprar.- EL MATERIAL. Para un mayor confort es importante que el asiento no nos de calor, es decir, que sea transpirable. Para ello, existen dos tipos de materiales idóneos: la malla, que permite el paso del aire; y la piel sintética, que es altamente transpirable. Elegir una u otra ya depende de cada persona.</p>
